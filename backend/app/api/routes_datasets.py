TRUTHY_VALUES = {"1", "true", "yes", "on"}


def _viewer_mode_enabled() -> bool:
    return str(os.getenv("COPYLOT_VIEWER_MODE", "")).strip().lower() in TRUTHY_VALUES


def _peptide_upload_dir() -> Path:
    base = Path(tempfile.gettempdir()) / "ProteomicsCoPYlot" / "peptide_uploads"
    base.mkdir(parents=True, exist_ok=True)
    return base


def _table_preview_response(stored) -> DatasetPreviewResponse:
    df = stored.frame
    return DatasetPreviewResponse(
        filename=stored.filename,
        kind=stored.kind,
        format=stored.filename.split(".")[-1].lower(),
        rows=len(df),
        columns=len(df.columns),
        columnNames=[str(c) for c in df.columns],
        preview=df.head(20).replace({float("nan"): None}).to_dict(orient="records"),
        suggestedIsLog2Transformed=getattr(
            stored, "suggested_is_log2_transformed", True
        ),
    )


def _peptide_response(stored) -> PeptidePathResponse:
    return PeptidePathResponse(
        filename=stored.filename,
        kind="peptide",
        format="path",
        path=stored.path,
        rows=0,
        columns=0,
        columnNames=[],
        preview=[],
    )


def _resolve_target_package(raw_package_name: str | None) -> str:
    text = str(raw_package_name or "").strip()
    if not text:
        return get_active_package()
    return set_active_package(text)


def _current_datasets_response() -> CurrentDatasetsResponse:
    current = get_all_current_datasets()

    protein = current["protein"]
    phospho = current["phospho"]
    phosprot = current["phosprot"]
    peptide = current["peptide"]

    return CurrentDatasetsResponse(
        activePackage=get_active_package(),
        availablePackages=list_packages(include_empty=not _viewer_mode_enabled()),
        activeMetadataProfile=get_active_metadata_profile(),
        availableMetadataProfiles=list_metadata_profiles(),
        autoGeneratedMetadataProfiles=list_auto_generated_profiles(),
        protein=_table_preview_response(protein) if protein else None,
        phospho=_table_preview_response(phospho) if phospho else None,
        phosprot=_table_preview_response(phosprot) if phosprot else None,
        peptide=_peptide_response(peptide) if peptide else None,
    )


def _ensure_profile_annotation_for_kind(
    *,
    kind: Literal["protein", "phospho"],
    profile_name: str,
    source_profile_name: str | None,
) -> None:
    current_dataset = get_current_dataset(kind)
    if current_dataset is None or not hasattr(current_dataset, "frame"):
        return

    uploaded_metadata = get_uploaded_metadata(kind, profile_name=profile_name)
    metadata_frame = (
        uploaded_metadata.frame
        if uploaded_metadata is not None and not uploaded_metadata.frame.empty
        else None
    )
    if metadata_frame is None:
        source_profiles = get_auto_generated_profile_sources(profile_name)
        if source_profiles is not None:
            left_name, right_name = source_profiles

            left_uploaded = get_uploaded_metadata(kind, profile_name=left_name)
            left_frame = (
                left_uploaded.frame
                if left_uploaded is not None and not left_uploaded.frame.empty
                else None
            )
            if left_frame is None:
                left_annotation = get_annotation(kind, metadata_profile_name=left_name)
                if left_annotation is not None and not left_annotation.metadata.empty:
                    left_frame = left_annotation.metadata

            right_uploaded = get_uploaded_metadata(kind, profile_name=right_name)
            right_frame = (
                right_uploaded.frame
                if right_uploaded is not None and not right_uploaded.frame.empty
                else None
            )
            if right_frame is None:
                right_annotation = get_annotation(kind, metadata_profile_name=right_name)
                if right_annotation is not None and not right_annotation.metadata.empty:
                    right_frame = right_annotation.metadata

            if left_frame is not None and right_frame is not None:
                combined = build_combined_metadata_frame(left_frame, right_frame)
                if not combined.empty:
                    metadata_frame = combined

    if metadata_frame is None or metadata_frame.empty:
        raise ValueError(
            f"Mixed metadata profile '{profile_name}' has no generated rows for {kind}. "
            "Generate both source metadata profiles first."
        )

    source_annotation = (
        get_annotation(kind, metadata_profile_name=source_profile_name)
        if source_profile_name
        else None
    )
    target_annotation = get_annotation(kind, metadata_profile_name=profile_name)
    reference_annotation = source_annotation or target_annotation

    is_log2_transformed = (
        reference_annotation.is_log2_transformed
        if reference_annotation is not None
        else bool(getattr(current_dataset, "suggested_is_log2_transformed", True))
    )
    filter_config = (
        reference_annotation.filter_config
        if reference_annotation is not None
        else AnnotationFilterConfig()
    )

    computed = compute_annotation_from_metadata(
        data=current_dataset.frame,
        metadata=metadata_frame,
        is_log2_transformed=is_log2_transformed,
        min_present=filter_config.minPresent,
        filter_mode=filter_config.mode,
    )
    save_annotation(
        kind=kind,
        source_data=current_dataset.frame,
        metadata=computed.metadata,
        log2_data=computed.log2_data,
        filtered_data=computed.filtered_data,
        is_log2_transformed=is_log2_transformed,
        metadata_source="uploaded",
        filter_config=filter_config,
        auto_detected=computed.auto_detected,
        warnings=computed.warnings,
        metadata_profile_name=profile_name,
    )


def _ensure_selected_profile_annotations(
    profile_name: str,
    *,
    source_profile_name: str | None,
) -> None:
    _ensure_profile_annotation_for_kind(
        kind="protein",
        profile_name=profile_name,
        source_profile_name=source_profile_name,
    )
    _ensure_profile_annotation_for_kind(
        kind="phospho",
        profile_name=profile_name,
        source_profile_name=source_profile_name,
    )


@router.post("/upload", response_model=DatasetPreviewResponse)
async def upload_dataset(
    file: UploadFile = File(...),
    kind: Literal["protein", "phospho", "phosprot"] = Form(...),
    package_name: str | None = Form(default=None, alias="packageName"),
) -> DatasetPreviewResponse:
    if not file.filename:
        raise HTTPException(status_code=400, detail="Missing filename")

    try:
        _ = get_extension(file.filename)
        df = read_dataframe(file.filename, file.file)
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e)) from e
    except Exception as e:
        raise HTTPException(status_code=400, detail=f"Failed to parse file: {e}") from e

    _resolve_target_package(package_name)

    # Re-uploading a dataset invalidates existing annotation state for that level.
    clear_annotation(kind, clear_all_profiles=True)
    if kind == "phospho":
        # Phosphoprotein data depends on phospho source + phospho annotation.
        clear_annotation("phosprot", clear_all_profiles=True)
        clear_dataset("phosprot")

    stored = save_table_dataset(file.filename, kind, df)
    if kind in ("protein", "phospho"):
        clear_uploaded_metadata(kind, clear_all_profiles=True)
    return _table_preview_response(stored)


@router.post("/peptide-path", response_model=PeptidePathResponse)
async def set_peptide_path(payload: PeptidePathRequest) -> PeptidePathResponse:
    if not payload.path.strip():
        raise HTTPException(status_code=400, detail="Path must not be empty")

    _resolve_target_package(payload.packageName)
    stored = save_peptide_path(payload.path)
    return _peptide_response(stored)


@router.post("/peptide-file", response_model=PeptidePathResponse)
async def upload_peptide_file(
    file: UploadFile = File(...),
    package_name: str | None = Form(default=None, alias="packageName"),
) -> PeptidePathResponse:
    if not file.filename:
        raise HTTPException(status_code=400, detail="Missing filename")

    _resolve_target_package(package_name)

    safe_name = Path(file.filename).name
    target = _peptide_upload_dir() / f"{uuid4().hex}_{safe_name}"

    try:
        with target.open("wb") as handle:
            shutil.copyfileobj(file.file, handle)
    except Exception as e:
        raise HTTPException(
            status_code=500,
            detail=f"Failed to store peptide file: {e}",
        ) from e

    stored = save_peptide_path(str(target.resolve()))
    # Keep the displayed dataset filename user-friendly (original selected file name),
    # while path points to the stored absolute temp file.
    stored.filename = safe_name
    return _peptide_response(stored)


