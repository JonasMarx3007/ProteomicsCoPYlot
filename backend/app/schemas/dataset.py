from typing import Any, Literal

from pydantic import BaseModel

DatasetKind = Literal["protein", "phospho", "phosprot", "peptide"]
TableDatasetKind = Literal["protein", "phospho", "phosprot"]


class DatasetPreviewResponse(BaseModel):
    filename: str
    kind: TableDatasetKind
    format: str
    rows: int
    columns: int
    columnNames: list[str]
    preview: list[dict[str, Any]]
    suggestedIsLog2Transformed: bool = True


class PeptidePathResponse(BaseModel):
    filename: str
    kind: Literal["peptide"]
    format: str
    path: str
    rows: int
    columns: int
    columnNames: list[str]
    preview: list[dict[str, Any]]


class CurrentDatasetsResponse(BaseModel):
    activePackage: str
    availablePackages: list[str]
    activeMetadataProfile: str
    availableMetadataProfiles: list[str]
    autoGeneratedMetadataProfiles: list[str]
    protein: DatasetPreviewResponse | None
    phospho: DatasetPreviewResponse | None
    phosprot: DatasetPreviewResponse | None
    peptide: PeptidePathResponse | None


class PeptidePathRequest(BaseModel):
    path: str
    packageName: str | None = None


class ActiveDatasetPackageRequest(BaseModel):
    packageName: str


class DatasetPackageCreateRequest(BaseModel):
    packageName: str


class DatasetPackageRenameRequest(BaseModel):
    oldName: str
    newName: str


class DatasetPackagesResponse(BaseModel):
    activePackage: str
    availablePackages: list[str]


class ActiveMetadataProfileRequest(BaseModel):
    profileName: str


class MetadataProfileCreateRequest(BaseModel):
    profileName: str


class MetadataProfileRenameRequest(BaseModel):
    oldName: str
    newName: str


class MetadataProfilesResponse(BaseModel):
    activeMetadataProfile: str
    availableMetadataProfiles: list[str]
